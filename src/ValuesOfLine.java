
public class ValuesOfLine {
	protected float x1;
	protected float y1;
	protected float x2;
	protected float y2;
	
	public ValuesOfLine(float x1,float y1,float x2,float y2)
	{
		this.x1 = x1;
		this.x1 = y1;
		this.x1 = x2;
		this.x1 = y2;
		System.out.println("the value of x1 : "+x1);
		System.out.println("the value of y1 : "+y1);
		System.out.println("the value of x2 : "+x2);
		System.out.println("the value of y2 : "+y2);
	}
	

}
